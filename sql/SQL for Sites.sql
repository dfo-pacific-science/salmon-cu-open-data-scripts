--sql to generate Sites files, Run on Reporting Server--

--Chinook--

SELECT
CUSS.SYSTEM_SITE AS CENSUS_SITE,
GFE.GAZETTED_NME AS GAZ_NAME,
CUSS.GFE_ID,
CUSS.Y_LAT,
CUSS.X_LONGT AS X_LONG,
CUSS.GFE_TYPE,
CUSS.FWA_WATERSHED_CDE,
CUSS.WATERSHED_CDE AS WS_CDE_50K,
CUSS.CU_NAME,
CUSS.FULL_CU_IN,
CUSS.SPECIES_QUALIFIED AS SP_QUAL,
CUSS.CU_TYPE,
CUSS.POP_ID,
CUSS.FAZ_ACRO,
CUSS.MAZ_ACRO,
CUSS.JAZ_ACRO
FROM
CONSERV_UNIT_SYSTEM_SITES_MV CUSS,
GEO_FEATURES GFE
WHERE
CUSS.GFE_ID=GFE.ID
AND
CUSS.SPECIES_QUALIFIED='CK'
ORDER BY FULL_CU_IN ASC;

